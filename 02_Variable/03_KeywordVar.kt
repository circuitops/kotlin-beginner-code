package `02_Variable`

fun main(){
    var message = "please release me"
    message = "im changed"          //Note: When using var keyword, the value of the variable name message is mutable or can be changed
    println(message)
}
/*
    Note:
    var - is the keyword
    message - variable name
    "please release me" = previous value of the variable name message
    "im changed" = new value of the variable name message
 */